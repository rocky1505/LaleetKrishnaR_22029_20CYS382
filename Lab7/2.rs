fn main() {
    // Declare two integer variables
    let a: i32 = 15;
    let b: i32 = 4;

    // Addition
    let addition = a + b;
    println!("Addition: {} + {} = {}", a, b, addition);

    // Subtraction
    let subtraction = a - b;
    println!("Subtraction: {} - {} = {}", a, b, subtraction);

    // Multiplication
    let multiplication = a * b;
    println!("Multiplication: {} * {} = {}", a, b, multiplication);

    // Division
    let division = a / b;
    println!("Division: {} / {} = {}", a, b, division);

    // Modulo (remainder of the division)
    let modulo = a % b;
    println!("Modulo: {} % {} = {}", a, b, modulo);
}

